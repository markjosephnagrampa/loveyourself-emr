	<h1 style="font-weight: bold; font-size: 50px;" align="center">About Us</h1>
	<p style="margin-top: 20px;">Self-worth is a powerful determinant of one’s quality of life.  It is the self-perception 
	of one’s right to achieve happiness and be given respect, of one’s ability to understand and 
	solve problems, and to triumph in the small and big challenges of life.</p>
	
	<p style="margin-top: 20px;">LoveYourself believes that self-worth is key to building an empowered community. We exist to 
	ignite in each one an active desire for a healthy and vibrant self-worth, especially among the 
	youth and males who have sex with males (MSMs).</p>
	
	<p style="margin-top: 20px;">Through awareness, education, counseling, fun social interactions, and activities that weave 
	these elements into a unity, we take delight in spreading our message of the value of self-worth and the critical importance 
	of loving oneself.</p>
	
	<p style="font-weight: bold; margin-top: 20px; font-size: 22px;">Our Mission</p>
	<p style="margin-top: 10px;">By embracing and nurturing our self-worth, we inspire others to do the same and create ripples 
	of positive change in the community.</p>
	
	<p style="font-weight: bold; margin-top: 20px; font-size: 22px;">Our Vision</p>
	<p style="margin-top: 10px;">A model community of MSM and friends who empower and affirm the self-worth of youth and MSM 
	in the Philippines.</p>
	
	<p style="font-weight: bold; margin-top: 40px; font-size: 22px;">What is loveyourself Inc. (the organization)?</p>
	<p style="margin-top: 20px;">Love Yourself Inc. (for the Youth & LGBT-MSM) is a community of volunteers that aim to reach out 
	to others to propagate ideas, attitudes, and practices that encourage loving oneself -- to DARE to be oneself, to CARE for oneself, 
	and to SHARE oneself as a way to multiplying joy.</p>
	
	<p style="font-weight: bold; margin-top: 20px; font-size: 19px;">DARE</p>
	<p style="margin-top: 10px;">• dare to be one's self
	<br>• dare to dive deep into the self and enhance self-awareness</br>
	• you cannot love someone you do not know -- so know one's self, truly, madly, deeply; then love yourself, truly, madly, deeply.</p>
	
	<p style="font-weight: bold; margin-top: 20px; font-size: 19px;">CARE</p>
	<p style="margin-top: 10px;">• care, actively pursue tending to one's needs and wants.
	<br>• enhance the natural well-being that we're blessed with, by allowing ourselves to enjoy the little and big things life offers, 
	experiences that make us</br>&nbsp&nbsp; joyful and feel good about ourselves.
	<br>• caring is also about ensuring that we avoid anything that harms our natural well-being, always mindful of our personal security and safety.</br></p>
	
	<p style="font-weight: bold; margin-top: 20px; font-size: 19px;">SHARE</p>
	<p style="margin-top: 10px;">• "Shared joy is double joy; shared sorrow is half a sorrow."
	<br>• When you love yourself, your life will become full of joy. Somebody who loves himself will find joy in sharing his time and talent, and will be</br> &nbsp&nbsp; surrounded by other generous and loving souls.
	<br>• When one is not exactly in a joyous situation, when one is fearful most especially, sharing one's self is a sure route back to joy. When you love</br> &nbsp&nbsp; yourself, you will engage a trusted partner 
	or friend so you can together look that fear in the eye and blast it to pieces with love.</p>
	
	<p style="font-weight: bold; margin-top: 40px; font-size: 22px;">What is The Loveyourself Project?</p>
	<p style="margin-top: 20px;">The Love Yourself Project is the ongoing pioneer project of  LoveYourself Inc., it aims to prevent the spread of 
	HIV/AIDS among the youth and key affected population through awareness, counseling and education.</p>
	
	<p style="font-weight: bold; margin-top: 20px; font-size: 19px;">Long-Term Goal:</p>
	<p style="margin-top: 10px;">Our goal is to open a self-sustaining resource centre for the YOUTH & LGBT-MSM that will provide education and 
	counselling utilizing social media networking, community involvement and advocacy focused but not limited to the risk and key population.</p>
	
	<p style="font-weight: bold; margin-top: 20px; font-size: 19px;">Short-Term Goals:</p>
	<p style="margin-top: 10px;">For the Youth:</p>
	<p>• To provide HIV Confidential Counseling and Testing (HCCT) to the YOUTH focused but not limited to LGBT-MSM who are the key population at risk for &nbsp&nbsp; HIV/STD.
	<br>• To establish activities promoting equality without regard to HIV status, religion, sex, gender preference and culture.</br>
	• To build self awareness as a basis for growth and self reliance of among the YOUTH & LGBT-MSM.</p>
	
	<p style="font-weight: bold; margin-top: 40px; font-size: 22px;">HISTORY</p>
	<p style="margin-top: 20px;">VINN Advocacy was founded by Twitter Shorty Awards Nurse of the Year Awardee, Ronivin (Vinn) Garcia Pagtakhan. He was joined by very close colleagues 
	and friends who were inspired by the increasing demand and queries by the Youth & LGBT-MSM through social media such as Twitter on STD, HIV,  sexuality and gender. Vinn (Ronivin) 
	thought that he could do more about these concerns, could touch more people than just by social media and could actually do something about it, thus consulted his close friend and 
	colleague James Garcia Bon and James Worrasaran who had the same thing on their mind, and had an experience with such advocacy during their stay in the United States where they 
	(James B.) took his Nurse Practitioner Residency.</p>
	<p style="margin-top: 20px;">Since then, the organization exponentially grew through social networking and was spearheaded by a core group of concerned and socially aware youth & 
	LGBT-MSM professionals and supporters with diverse ideas and preferences yielding to the goal to minimize the morbidity rate and in the near future to completely eradicate venereal 
	infections (STD, HIV and AIDS), help the youth & high risk LGBT-MSM in their battle with confusion, prejudice and respect, and ultimately open a safe haven and resource centre for 
	the LGBT-MSM here in the Philippines.</p>
	
